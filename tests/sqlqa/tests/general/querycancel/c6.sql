control query default CANCEL_MINIMUM_BLOCKING_INTERVAL '1';

set schema TEST_SCHEMA_2;

prepare xx from 
select [last 1]
    c_custkey,
    c_name,
      cast(sum(l_extendedprice * (1 - l_discount)) as numeric(18,2)) as revenue,
      cast(c_acctbal as numeric(18,2)),
    n_name,
    c_address,
    c_phone,
    c_comment
from
    customer,
    orders,
    lineitem,
    nation
where
    c_custkey = o_custkey
    and l_orderkey = o_orderkey
    and o_orderdate >= date '1994-07-01'
    and o_orderdate < date '1994-07-01' + interval '3' month
    and l_returnflag = 'R'
    and c_nationkey = n_nationkey
group by
    c_custkey,
    c_name,
    c_acctbal,
    c_phone,
    n_name,
    c_address,
    c_comment
order by
    revenue desc;;

infostats xx;

execute xx;

get statistics for qid current;

exit